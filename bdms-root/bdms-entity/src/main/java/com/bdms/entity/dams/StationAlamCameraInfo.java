public class StationAlamCameraInfo implements Serializable {

	private static final long serialVersionUID = -2877840078486790528L;

	private Station station;

	private Integer levelValue;

	private Integer enterTimes;

	private String level;

	public Station getStation() {
		return station;
	}

	public void setStation(Station station) {
		this.station = station;
	}

	public Integer getLevelValue() {
		return levelValue;
	}

	public void setLevelValue(Integer levelValue) {
		this.levelValue = levelValue;
	}

	public Integer getEnterTimes() {
		return enterTimes;
	}

	public void setEnterTimes(Integer enterTimes) {
		this.enterTimes = enterTimes;
	}

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

}
