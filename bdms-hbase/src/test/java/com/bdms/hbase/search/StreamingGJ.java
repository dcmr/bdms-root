package com.bdms.hbase.search;

public class StreamingGJ {
	
	private String station_id;
	private String start_time;
	private String enter_times;
	private String exit_times;
	
	
	public StreamingGJ() {
		super();
	}

	public StreamingGJ(String station_id, String start_time,
			String enter_times, String exit_times) {
		super();
		this.station_id = station_id;
		this.start_time = exit_times;
		this.enter_times = exit_times;
		this.exit_times = exit_times;
	}

	public String getStation_id() {
		return station_id;
	}

	public void setStation_id(String station_id) {
		this.station_id = station_id;
	}

	public String getStart_time() {
		return start_time;
	}

	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}

	public String getEnter_times() {
		return enter_times;
	}

	public void setEnter_times(String enter_times) {
		this.enter_times = enter_times;
	}

	public String getExit_times() {
		return exit_times;
	}

	public void setExit_times(String exit_times) {
		this.exit_times = exit_times;
	}

	public String toString() {
		return "StreamingGJ [station_id=" + station_id + ", start_time="
				+ start_time + ", enter_times=" + enter_times + ", exit_times="
				+ exit_times + "]";
	}
	
	
}
